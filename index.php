                <h3>Bio</h3>
                <p>Dickie Bird's white cap, twitching shoulders and forearm stretch became as much a part of the day out as the battle between bat and ball. He seemed to attract minor incident, without ever allowing the day to be soured by controversy. Burst pipes, reflecting windscreens and bomb scares all conspired to trouble him, but each impostor was met with humour and Yorkshire-bred common sense. Notorious for being an early Bird (he once made it to The Oval at 6am, so nervous was he about being late and was five-hours early to meet the Queen on one occasion), and plagued by bad weather he made the transition from cricketing figure to something close to a national institution.</p>

                <p class="sep">A favourite among the public who took to his idiosyncrasies, Bird was equally admired and respected by the players. He diffused many a situation that a lesser umpire may have allowed to escalate, typically with common sense and good humour. Most importantly, he simply made few mistakes. "They all rated me the best: Sobers, Richards, Lillee and Botham. That means a lot I can tell you," he admitted.</p>



                <p>Harold "Dickie" Bird was born in Barnsley in April 1933. 5'10" and a right-hand batsman, Bird played in a Barnsley side that included Geoff Boycott and the journalist Michael Parkinson, with whom he remained friends. A good enough player to have represented Yorkshire during their period of dominance (debuting in 1956 against Scotland), a regular first team place eluded him in such a strong side (his career best performance 181* against Glamorgan at Bradford in 1959 was rewarded with omission from the next game as the senior players returned from Test duty) and he moved to Leicestershire at the end of that year. He won his county cap in his first season, 1960, in which he topped 1,000 first-class runs, but later referred to the decision to leave Yorkshire as "the biggest mistake of my life".</p>

                <p class="sep">Bird retired relatively early, aged just 32, and spent a few years coaching and playing as a club professional. He later admitted to regret that his playing career had not been more successful (first-class average: 20.71): "I just wish I'd have believed in myself as a batsman the way I do as an umpire," he told The Cricketer (November 1998) during his final match as an umpire. "I had the ability I can tell you. If you had compared me to Boycott in the nets you would have picked me as the Test player. Ray Illingworth said I played as straight as anyone he'd ever seen. Umpiring has been good to me, but it is the second-best thing to playing."</p>



                <p>Bird officiated his first game in 1970, with his first Test coming against New Zealand at Leeds in 1973. In 1992 he stood in Zimbabwe's inaugural Test as the first ICC panel umpire. A guard of honour by the players and a standing ovation from the crowd as England took on India in 1996 marked his final Test, at his beloved Lord's. Bird, always an emotional man, was reduced to tears. In all he officiated in 66 Tests and 69 One-Day Internationals, overtaking Frank Chester's record (of 48 Tests) in Zimbabwe in 1996. His final first-class game was at Headingley, between Yorkshire and Warwickshire in 1998.
                </p>
                <p class="sep">Bird was awarded the MBE in June 1986 in the Queen's Birthday Honours List ("It means more to me than my life," he commented) and became a frequent tea-time visitor at the Palace, apparently attending more than 20 times. He gained other remarkable friends, including former Prime Minister John Major, billionaire cricket fanatic John Paul Getty and the thriller writer Stephen King. "I wish I'd had a family. That's where I missed out in life," he lamented on his retirement but his oft-repeated phrase was that he was "married to the game."</p>



                <p>Bird's real legacy will be top-quality umpiring. Calm, despite his nervous disposition; consistent, despite his erratic body movements; and unimpeachably impartial despite his obvious love for all things Yorkshire and England, Bird added to the enjoyment of the spectators without ever detracting from the cricket. Maybe a little reluctant to give lbw decisions, (he would argue "the Laws state I have to be certain"), he has expressed reservations about the marginalisation of umpires by technology in recent times.</p>

                <p>A busy retirement, in which he revels in the new role of a media personality, has seen the ubiquitous Bird appear on quiz and chat shows, embark on a speaking tour where his routine of anecdotes provokes great amusement and improve upon each telling. He has produced several books, including My Autobiography with Keith Lodge, the biggest selling sports book of all time and the follow-up White Cap and Bails, another best-seller.</p>
